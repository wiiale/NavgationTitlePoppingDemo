import UIKit

extension UIColor {
    
    public static var nav_purple: UIColor {
        return UIColor(named: "purple") ?? UIColor.purple
    }
    
    public static var nav_black: UIColor {
        return UIColor(named: "black") ?? UIColor.black
    }
}
