import UIKit

extension UIFont {
    public static var nav_regular: UIFont {
        return .systemFont(ofSize: 18, weight: .medium)
    }
    
    public static var nav_small: UIFont {
        return .systemFont(ofSize: 13, weight: .medium)
    }
}
